#include <iostream>

using namespace std;

int main() {
  int number = 3;
  float divideByTwo = number / 2.0;
  float addThree = divideByTwo + 3;

  cout << "Starting Number  = "  << number << endl;
  cout << "Divide By Two  = "  << divideByTwo << endl;
  cout << "Add Three  = "  << addThree << endl;

  return 0;
}